     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {

        $posts = new Posts;
        $posts->name = $request->name;
        $posts->content = $request->content;

        $posts->save();
        return redirect()->route('admin.posts.index');

    }

